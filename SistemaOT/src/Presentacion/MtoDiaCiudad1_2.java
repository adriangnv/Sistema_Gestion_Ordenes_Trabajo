    private void cargarTexto(int indice) {

        if (ciudades.size() > 0) {
            this.btnEditar.setEnabled(true);
            ciudad = ciudades.get(indice);
            this.txtCodigo.setText(Integer.toString(ciudad.getIdCiudad()));
            this.txtNombre.setText(ciudad.getCiudad());
            this.txtCP.setText(Integer.toString(ciudad.getCp()));
            this.jComboBox1.setSelectedIndex(ciudades.get(indice).getProvincia().getIdProvincia() - 1);

        } else {
            limpiarCajasTexto();
            this.btnEditar.setEnabled(false);
               }
       }

    private void listarJcombo() {

        try {
            provincias = new ArrayList<Provincia>(new ProvinciaDAO().buscarTodo());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getLocalizedMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        jComboBox1.setModel(new ListComboBoxModel(provincias));
    }

    private void cargarTabla() {
       
        ModeloTabla tabla = new ModeloTabla();

        try {
            ciudades = new ArrayList<Ciudad>(new CiudadDAO().buscarTodo());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getLocalizedMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        tabla.addColumn("Id Ciudad");
        tabla.addColumn("Nombre");
        tabla.addColumn("Codigo Postal");
        tabla.addColumn("Provincia");
        tabla.setRowCount(ciudades.size());

        int i = 0;
        for (Ciudad ciud : ciudades) {
            tabla.setValueAt(ciud.getIdCiudad(), i, 0);
            tabla.setValueAt(ciud.getCiudad(), i, 1);
            tabla.setValueAt(ciud.getCp(), i, 2);
            tabla.setValueAt(ciud.getProvincia().getProvincia(), i, 3);
            i++;
        }
        this.jTable1.setModel(tabla);

    }

    public void limpiarCajasTexto() {

        this.txtCodigo.setText("");
        this.txtNombre.setText("");
        this.txtCP.setText("");
        this.jComboBox1.setSelectedIndex(-1);

    }
    
