    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed


        CiudadDAO nciudad = new CiudadDAO();
        Valida validar = new Valida();
        if (nuevaciudad == true) {
            ciudad = new Ciudad();

            if (validar.isCampoVacio(txtNombre.getText())) {
                JOptionPane.showMessageDialog(null,"¡Faltan Completar Datos!","Error" , JOptionPane.ERROR_MESSAGE);
                txtNombre.requestFocus();
            } else {
                if (validar.isCampoVacio(txtCP.getText())) {
                    JOptionPane.showMessageDialog(null,"¡Faltan Completar Datos!","Error" , JOptionPane.ERROR_MESSAGE);
                    txtCP.requestFocus();
                } else {
                    if (jComboBox1.getSelectedIndex() == -1) {
                        JOptionPane.showMessageDialog(null, "¡Selecciones un item!","Error" , JOptionPane.ERROR_MESSAGE);
                        jComboBox1.showPopup();

                    } else {
                        ciudad.setCiudad(this.txtNombre.getText());
                        ciudad.setCp(Integer.parseInt(this.txtCP.getText()));
                        ciudad.setProvincia((Provincia) this.jComboBox1.getSelectedItem());
                        try {
                            nciudad.agregar(ciudad);
                        } catch (SQLException ex) {
                            Logger.getLogger(MtoDiaCiudad1_2.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        this.cargarTabla();
                        indice = (ciudades.size() - 1);
                        this.cargarTexto(indice);
                        this.btnNuevo.setEnabled(true);
                        this.btnEditar.setEnabled(true);
                        this.btnGrabar.setEnabled(false);
                        this.btnCancelar.setEnabled(false);
                        this.habilitacajatexto(false);
                        this.jTable1.setEnabled(true);
                        habilitaclick = false;
                    }
                }
            }

        } else {
        
            if (validar.isCampoVacio(txtNombre.getText())) {
                JOptionPane.showMessageDialog(null,"¡Faltan Completar Datos!","Error" , JOptionPane.ERROR_MESSAGE);
                txtNombre.requestFocus();
            } else {
                if (validar.isCampoVacio(txtCP.getText())) {
                    JOptionPane.showMessageDialog(null,"¡Faltan Completar Datos!","Error" , JOptionPane.ERROR_MESSAGE);
                    txtCP.requestFocus();
                } else {
                    if (jComboBox1.getSelectedIndex() == -1) {
                        JOptionPane.showMessageDialog(null, "¡Selecciones un item!","Error" , JOptionPane.ERROR_MESSAGE);
                        jComboBox1.showPopup();

                    } else {
                        ciudad.setIdCiudad(Integer.parseInt(this.txtCodigo.getText()));
                        ciudad.setCiudad(this.txtNombre.getText());
                        ciudad.setCp(Integer.parseInt(this.txtCP.getText()));
                        ciudad.setProvincia((Provincia) this.jComboBox1.getSelectedItem());
                        try {
                            nciudad.actualizar(ciudad);
                        } catch (SQLException ex) {
                            // VER ESTO EN LOS FORMULARIOS!!
                            Logger.getLogger(MtoDiaCiudad1_2.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        this.cargarTabla();
                        this.cargarTexto(indice);
                        this.habilitacajatexto(false);
                        this.btnNuevo.setEnabled(true);
                        this.btnEditar.setEnabled(true);
                        this.btnGrabar.setEnabled(false);
                        this.btnCancelar.setEnabled(false);
                        this.jTable1.setEnabled(true);
                        habilitaclick = false;
                        this.btnNuevo.requestFocus();
                        
                    }
                }
            }
        }
                        
}//GEN-LAST:event_btnGrabarActionPerformed

