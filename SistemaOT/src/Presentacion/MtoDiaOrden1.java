    private void cargarTexto(int indice) {

        if (ordenes.size() > 0) {

            orden = ordenes.get(indice);

            //this.txtUsuario.setText(orden.getUsuario().getUsuario());
            this.txtNumero.setText(Integer.toString(orden.getIdOrden()));
            this.jComboBox1.setSelectedIndex(ordenes.get(indice).getEquipo().getIdEquipo() - 1);
            this.txtDescEquipo.setText(orden.getEquipo().getDescripcion());
            this.jComboBox2.setSelectedIndex(ordenes.get(indice).getEquipo().getCliente().getIdCliente() - 1);
            this.txtRespCliente.setText(orden.getEquipo().getCliente().getResponsable());
            this.txtObservaciones.setText(orden.getObservacion());
            this.txtRemito.setText(orden.getRemito());
            this.jXDatePicker1.setDate(orden.getFechaRecep());
            this.jXDatePicker2.setDate(orden.getFechaLimite());
            this.jXDatePicker3.setDate(orden.getFechaTrabajo());
            this.jXDatePicker4.setDate(orden.getFechaEntrega());
            this.txtUsuario.setText(orden.getUsuario().getUsuario());
            this.txtEstado.setText(orden.getEstado());
            if (this.txtEstado.getText().equals("Pendiente")) {
                this.txtEstado.setBackground(Color.red);
            }
            if (this.txtEstado.getText().equals("Concluido")) {
                this.txtEstado.setBackground(Color.YELLOW);
            }
            if (this.txtEstado.getText().equals("Entregado")) {
                this.txtEstado.setBackground(Color.GREEN);
            }
            estado();


        } else {

            limpiarCajasTexto();
            this.btnEditar.setEnabled(false);
            this.btnGrabar.setEnabled(false);
            this.btnCancelar.setEnabled(false);

        }
    }

    public void estado() {
        if ((jXDatePicker4.getDate()) != null) {
            this.txtEstado.setText("Entregado");
            estado1 = "Entregado";
            this.txtEstado.setBackground(Color.GREEN);
        } else {
            if ((jXDatePicker3.getDate()) != null) {
                this.txtEstado.setText("Concluido");
                estado1 = "Concluido";
                this.txtEstado.setBackground(Color.YELLOW);
            } else {
                if ((jXDatePicker1.getDate()) != null) {
                    this.txtEstado.setText("Pendiente");
                    estado1 = "Pendiente";
                    this.txtEstado.setBackground(Color.red);
                }
            }
        }
    }

    private void listar() {

        try {
            ordenes = new ArrayList<Orden>(new OrdenDAO().buscarTodo());
            equipos = new ArrayList<Equipo>(new EquipoDAO().buscarTodo());
            clientes = new ArrayList<Cliente>(new ClienteDAO().buscarTodo());

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getLocalizedMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }

        jComboBox1.setModel(new ListComboBoxModel(equipos));

        jComboBox2.setModel(new ListComboBoxModel(clientes));


    }

