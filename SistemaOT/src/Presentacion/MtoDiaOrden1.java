    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        OrdenDAO norden = new OrdenDAO();

        if (nuevaorden == true) {
            if (jComboBox1.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(null, "¡Selecciones un item!", "Error", JOptionPane.ERROR_MESSAGE);
                jComboBox1.showPopup();

            } else {
                if (this.jXDatePicker2.getDate() == null) {
                    JOptionPane.showMessageDialog(null, "¡Faltan Completar Datos!", "Error", JOptionPane.ERROR_MESSAGE);
                    this.jXDatePicker2.requestFocus();

                } else {
                    orden.setUsuario(usuario);
                    orden.setEquipo((Equipo) this.jComboBox1.getSelectedItem());
                    orden.setFechaRecep(convertirFecha(this.jXDatePicker1.getDate()));
                    //System.out.println(estado1);
                    orden.setEstado(estado1);
                    //System.out.println(this.txtEstado.getText());
                    orden.setFechaLimite(convertirFecha(this.jXDatePicker2.getDate()));
                    orden.setFechaTrabajo(null);
                    orden.setFechaEntrega(null);
                    orden.setObservacion(this.txtObservaciones.getText());
                    orden.setRemito(null);
                    //System.out.println(orden);
                    try {
                        norden.agregar(orden);
                    } catch (SQLException ex) {
                        Logger.getLogger(MtoDiaCiudad1_2.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    listar();
                    indice = (ordenes.size() - 1);
                    cargarTexto(indice);


                    this.btnEditar.setEnabled(true);
                    this.btnGrabar.setEnabled(false);
                    this.btnCancelar.setEnabled(false);
                    this.btnNuevo.setEnabled(true);
                    this.btnAtras.setEnabled(true);
                    this.btnAdelante.setEnabled(true);
                    this.btnBuscar.setEnabled(true);
                    this.jComboBox1.setEnabled(false);
                    this.jComboBox2.setEnabled(false);
                    this.jXDatePicker2.setEnabled(false);
                    this.jButton1.setEnabled(false);
                    this.txtObservaciones.setEnabled(false);
                }
            }
        } else {

            orden = ordenes.get(indice);
            
                if ((this.jXDatePicker3.isEnabled() == true)&(this.jXDatePicker3.getDate() == null)) {
                    JOptionPane.showMessageDialog(null, "¡Faltan Completar Datos 1!", "Error", JOptionPane.ERROR_MESSAGE);
                    this.jXDatePicker3.requestFocus();
                }else{
              if ((this.txtRemito.isEnabled()==true)&(this.txtRemito.getText().isEmpty())){
                    JOptionPane.showMessageDialog(null, "¡Faltan Completar Datos 2!", "Error", JOptionPane.ERROR_MESSAGE);
                    this.txtRemito.requestFocus();
                } else {
                    if ((this.jXDatePicker4.isEnabled()==true)&(this.jXDatePicker4.getDate() == null)) {
                        JOptionPane.showMessageDialog(null, "¡Faltan Completar Datos 3!", "Error", JOptionPane.ERROR_MESSAGE);
                        this.jXDatePicker4.requestFocus();
                    } else {
            orden.setIdOrden(Integer.parseInt(this.txtNumero.getText()));
            orden.setUsuario(usuario);
            orden.setEquipo((Equipo) this.jComboBox1.getSelectedItem());
            java.util.Date utilDate = this.jXDatePicker3.getDate();
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            orden.setFechaTrabajo(sqlDate);
            orden.setEstado(estado1);
            if (this.jXDatePicker4.getDate() == null) {
                orden.setFechaEntrega(null);
            } else {
                java.util.Date utilDate1 = this.jXDatePicker4.getDate();
                java.sql.Date sqlDate1 = new java.sql.Date(utilDate1.getTime());
                orden.setFechaEntrega(sqlDate1);
            }
            orden.setObservacion(this.txtObservaciones.getText());
            orden.setRemito(this.txtRemito.getText());


            try {
                norden.actualizar(orden);
            } catch (SQLException ex) {
                Logger.getLogger(MtoDiaCiudad1_2.class.getName()).log(Level.SEVERE, null, ex);
            }


            this.cargarTexto(indice);

            this.btnNuevo.setEnabled(true);
            this.btnEditar.setEnabled(true);
            this.btnAtras.setEnabled(true);
            this.btnAdelante.setEnabled(true);
            this.btnBuscar.setEnabled(true);
            this.btnGrabar.setEnabled(false);
            this.btnCancelar.setEnabled(false);
            this.jComboBox1.setEnabled(false);
            this.jComboBox1.setEnabled(false);
            this.jXDatePicker3.setEnabled(false);
            this.jXDatePicker4.setEnabled(false);
            this.txtRemito.setEnabled(false);
            this.jButton1.setEnabled(false);
            this.txtObservaciones.setEnabled(false);
            }
            }
          }
       }
    
    }//GEN-LAST:event_btnGrabarActionPerformed

