    private void cargarTabla() {
        
        ModeloTabla tabla = new ModeloTabla();
        
            equipos1 = new ArrayList<Equipo>(equipos);
        
         

        tabla.addColumn("N째 de id");
        tabla.addColumn("N째 de Equipo");
        tabla.addColumn("Descripci처n");
        
        tabla.setRowCount(equipos1.size());

        int i = 0;
        for (Equipo eq : equipos1) {
            tabla.setValueAt(eq.getIdEquipo(), i, 0);
            tabla.setValueAt(eq.getNroEquipo(), i, 1);
            tabla.setValueAt(eq.getDescripcion(), i, 2);
            
            i++;
        }
        this.jTable1.setModel(tabla);

    }
    

